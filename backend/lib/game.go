func WheelResult(risk string, segments uint8, index float64) float64 {
	var result float64
	if risk == "low" {
		result = lowSegment[segments][int(index)]
	} else if risk == "medium" {
		result = mediumSegment[segments][int(index)]
	} else if risk == "high" {
		result = highSegment[segments][int(index)]
	}

	return result
}
